package com.example.myapplication2;

import android.content.Context;
import android.database.sqlite.SQLiteDatabase;
import android.database.sqlite.SQLiteOpenHelper;
import android.util.Log;


public class DatabaseHelper extends SQLiteOpenHelper {
	private static final String DATABASE_NAME="BDRegistros";
	public static final String HORAS="horas";
	public static final String Reg="reg";
	public static final String Frec="frec";
	public static final String FrMa="frma";
	public static final String FrMi="frmi";
	public static final String Pre="pre";
	
    //////////////////////////////////////////////////////////////////////////////////////
    //////////////////////////////////////////////////////////////////////////////////////
    //////////////////////////////////////////////////////////////////////////////////////
    //////////////////////////////////////////////////////////////////////////////////////
    public DatabaseHelper(Context context) {
    	super(context,DATABASE_NAME,null,1);
    }
    //////////////////////////////////////////////////////////////////////////////////////
    //////////////////////////////////////////////////////////////////////////////////////
    //////////////////////////////////////////////////////////////////////////////////////
    //////////////////////////////////////////////////////////////////////////////////////
	@Override
	public void onCreate(SQLiteDatabase db){
		try{
		db.execSQL("CREATE TABLE Electros(horas String,reg String,frec String,frma String,frmi String,pre String);");
		}catch (Exception e)
    	{
	     Log.d("Basededatos","Exception initDB: "+e.toString());	
	    }
		//db.execSQL("CREATE TABLE registros(horas String,reg String,frec String)");
	}
    //////////////////////////////////////////////////////////////////////////////////////
    //////////////////////////////////////////////////////////////////////////////////////
    //////////////////////////////////////////////////////////////////////////////////////
    //////////////////////////////////////////////////////////////////////////////////////
	@Override
	public void onUpgrade(SQLiteDatabase db,int oldVersion, int newVersion){
		//android.util.log.w("registros","Upgrading database, " +
		//		"which will destroy all old data");
		db.execSQL("DROP TABLE IF EXIST Electros");
		onCreate(db);		
	}
}
